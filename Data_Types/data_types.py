#string
a = "I love coding"
type_a = type(a)

print(f"{a} is a {type_a}")

#get length of string
length_a = len(a)
print(f"Length of {a} is {length_a}")

#split string
split_a = a.split()
print(f"Split {a} is {split_a}")


#integer
